uint8_t crc8_ccitt_buff(uint8_t crc, uint8_t *buff, int len)
{
	int i;
	for (i = 0; i < len; i++) {
		crc = crc8_ccitt_byte(crc, buff[i]);
	}
	return crc;
}

uint16_t crc16_ccitt_buff(uint16_t crc, uint8_t *buff, int len)
{
	int i;
	for (i = 0; i < len; i++) {
		crc = crc16_ccitt_byte(crc, buff[i]);
	}
	return crc;
}

static const uint8_t crc8_testin1[] = { 0x55, 0xAA, 0X33, 0X77, 0XFF, 0X00 };
static const uint8_t crc8_excepted1 = 0x7E;
static const uint8_t crc8_testin2[] = { 0XED, 0XBA, 0XF2, 0XC0, 0X90 };
static const uint8_t crc8_excepted2 = 0x65;

void crc8_ccitt_selftest(void)
{
	assert(crc8_ccitt_buff(0x00, (uint8_t *)crc8_testin1,
			       sizeof(crc8_testin1)) == crc8_excepted1);
	assert(crc8_ccitt_buff(0x00, (uint8_t *)crc8_testin2,
			       sizeof(crc8_testin2)) == crc8_excepted2);
}

